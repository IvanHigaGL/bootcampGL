package com.globallogic.app;


public class TiposPrimitivos 
{
    public static void main( String[] args )
    {
    	int myNum = 15;
    	char myChar = 'a';
    	boolean myBool = true;
    	float myFloat = (float)0.1;
    	short myShort = 500;
    	System.out.println("El valor de mi numero es " + myNum);
    	System.out.println("El valor de mi char es " + myChar);
    	System.out.println("El valor de mi boolean es " + myBool);
    	System.out.println("El valor de mi float es " + myFloat);
    	System.out.println("El valor de mi short es " + myShort);
    }
}
